function clampScore(n: unknown): number {
  if (typeof n === "number" && Number.isFinite(n))
    return Math.max(0, Math.min(1, n));
  if (typeof n === "string") {
    const v = Number(n.trim());
    if (Number.isFinite(v)) return Math.max(0, Math.min(1, v));
  }
  return 0;
}

function sanitizeModerationTextField(
  raw: unknown,
  maxLen: number,
): string | undefined {
  if (typeof raw !== "string") return undefined;
  const flattened = raw.replace(/\s+/g, " ").replace(/[<>]/g, "").trim();
  if (
    !flattened ||
    /^none\.?$/i.test(flattened) ||
    /^safe\.?$/i.test(flattened)
  )
    return undefined;
  /** Never bubble raw floats that look like model scores through to humans. */
  if (/\b0\.\d{2,}\b/.test(flattened)) return undefined;
  return flattened.replace(/[.。]+$/, "").slice(0, maxLen);
}

function sanitizeAiReason(raw: unknown): string | undefined {
  return sanitizeModerationTextField(raw, 220);
}

function sanitizeInternalModerationNotes(raw: unknown): string | undefined {
  return sanitizeModerationTextField(raw, 4000);
}

function sanitizeUserSummary(raw: unknown): string | undefined {
  return sanitizeModerationTextField(raw, 620);
}

function parseDetectedLabels(raw: unknown): string[] {
  if (!Array.isArray(raw)) return [];
  return raw
    .filter((v): v is string => typeof v === "string" && v.trim().length > 0)
    .map((v) => v.trim().toLowerCase());
}

/**
 * Context-aware adjustments:
 * - Violence labels: prevent blood-on-skin from being scored as nudity.
 * - Nature / waterfalls: damp nudity & suggestive scores when there's no human subject cue.
 */
function applyContextSignals(
  scores: ParsedModeration,
  labels: string[],
): ParsedModeration {
  const VIOLENCE_OBJECTS = [
    "knife",
    "weapon",
    "gun",
    "pistol",
    "rifle",
    "sword",
    "blood",
    "injury",
    "wound",
    "fight",
    "fighting",
    "punch",
    "kick",
    "stab",
    "stabbing",
    "gore",
    "corpse",
    "dead",
    "shooting",
  ];
  const SKIN_FALSE_POS = ["blood", "wound", "injury", "bruise", "cut", "stab"];

  const NATURE_SCENE = [
    "waterfall",
    "waterfalls",
    "mist",
    "misty",
    "spray",
    "cascade",
    "rapid",
    "rapids",
    "river",
    "stream",
    "ocean",
    "wave",
    "waves",
    "surf",
    "sand",
    "rock",
    "rocks",
    "stone",
    "cliff",
    "mountain",
    "forest",
    "trees",
    "vegetation",
    "landscape",
    "lake",
    "rainbow",
    "snowfield",
    "glacier",
  ];

  let adj = { ...scores };

  const hasViolenceObject = labels.some((l) =>
    VIOLENCE_OBJECTS.some((v) => l.includes(v)),
  );
  const hasSkinFalsePositive = labels.some((l) =>
    SKIN_FALSE_POS.some((v) => l.includes(v)),
  );

  if (hasViolenceObject) {
    adj.violence = Math.min(1, adj.violence + 0.15);
  }
  if (hasViolenceObject || hasSkinFalsePositive) {
    if (hasSkinFalsePositive && adj.violence > adj.nudity * 0.6) {
      const reductionFactor = 0.55;
      adj.nudity = Math.max(0, adj.nudity * reductionFactor);
      adj.sexual = Math.max(0, adj.sexual * reductionFactor);
      adj.suggestive = Math.max(0, adj.suggestive * reductionFactor);
    }
  }

  const labelBlob = labels.join(",").toLowerCase();
  const hasNatureScene = labels.some((l) =>
    NATURE_SCENE.some((w) => l.includes(w)),
  );
  const hasPersonCue =
    /\b(person|people|human|humans|portrait|faces?|woman|women|girl|girls|man|men|boy|boys|child|children|infants?|shirtless|selfie)\b/i.test(
      labelBlob,
    );

  if (hasNatureScene && !hasPersonCue) {
    const nudCap = 0.2;
    const sexCap = 0.22;
    const sugCap = 0.28;
    if (adj.nudity > nudCap) adj.nudity = nudCap;
    if (adj.sexual > sexCap) adj.sexual = sexCap;
    if (adj.suggestive > sugCap) adj.suggestive = sugCap;
  }

  adj.safe =
    1 -
    Math.max(adj.nudity, adj.sexual, adj.suggestive, adj.violence, adj.hate);
  return adj;
}

/** Normalize a MIME type string to one Gemini accepts for inline image data. */
function normalizeImageMime(mime: string): "image/jpeg" | "image/png" {
  const m = (mime ?? "").toLowerCase().split(";")[0].trim();
  if (m === "image/png") return "image/png";
  // Accept jpeg, jpg, webp, gif, bmp — all converted to jpeg label since
  // ffmpeg always emits JPEG frames and S3 headers may vary.
  return "image/jpeg";
}

/**
 * Validate and clean a base64 string before it is sent to Gemini.
 * Strips the data-URL prefix if present and rejects strings that are too
 * small to represent a real image (empty, whitespace, or stub data).
 * Throws if the input cannot be made safe.
 */
function validateAndCleanBase64(base64: string, context: string): string {
  if (!base64) throw new Error(`[${context}] Missing base64 data`);

  let clean = base64;
  if (clean.startsWith("data:")) {
    clean = clean.replace(/^data:[^;]+;base64,/, "");
  }
  clean = clean.trim();

  if (clean.length < 1_000) {
    throw new Error(
      `[${context}] Base64 too small (${clean.length} chars) — likely invalid frame`,
    );
  }

  return clean;
}

function parseSeverityVm(raw: unknown): string | undefined {
  if (typeof raw !== "string") return undefined;
  const v = raw.trim().toLowerCase();
  if (v === "low" || v === "medium" || v === "high") return v;
  return undefined;
}

function parseEvidenceArrayVm(raw: unknown): string[] | undefined {
  if (!Array.isArray(raw)) return undefined;
  const items = raw
    .filter((x): x is string => typeof x === "string")
    .map((s) => s.replace(/\s+/g, " ").replace(/[<>]/g, "").trim())
    .filter((s) => s.length > 2 && s.length < 300);
  return items.length > 0 ? items.slice(0, 5) : undefined;
}

function scoresFromParsedRecord(
  parsed: Record<string, unknown>,
): ParsedModeration {
  const detectedLabels = parseDetectedLabels(parsed.detectedLabels);
  const internalRaw =
    parsed.reasonInternal ??
    parsed.internalExplanation ??
    parsed.internalNotes ??
    parsed.moderatorNotes;

  const primaryCategoryRaw = parsed.primary_category ?? parsed.primaryCategory;
  const confidenceRaw = parsed.confidence;
  const severityRaw = parsed.severity;
  const evidenceRaw = parsed.evidence;

  const categoriesObj = parsed.categories as
    | Record<string, unknown>
    | undefined;

  const base: ParsedModeration = {
    nudity: clampScore(categoriesObj?.nudity ?? parsed.nudity),
    sexual: clampScore(categoriesObj?.sexual_content ?? parsed.sexual),
    suggestive: clampScore(parsed.suggestive),
    violence: Math.max(
      clampScore(categoriesObj?.violence ?? parsed.violence),
      clampScore(categoriesObj?.gore ?? 0),
      clampScore(categoriesObj?.weapons ?? 0),
    ),
    hate: clampScore(categoriesObj?.harassment ?? parsed.hate),
    spam: clampScore(parsed.spam),
    safe: clampScore(parsed.safe),
    bodyPartFocus: parsed.bodyPartFocus === true,
    highSkinRatio: parsed.highSkinRatio === true,
    detectedLabels,
    reason: sanitizeAiReason(parsed.reason ?? parsed.explanation),
    userSummary: sanitizeUserSummary(
      parsed.userSummary ?? parsed.authorSummary ?? parsed.summaryForAuthor,
    ),
    reasonInternal: sanitizeInternalModerationNotes(internalRaw),
    primaryCategory:
      typeof primaryCategoryRaw === "string"
        ? primaryCategoryRaw.trim().toLowerCase() === "none" ||
          primaryCategoryRaw.trim() === ""
          ? undefined
          : primaryCategoryRaw.trim().toLowerCase()
        : undefined,
    evidence: parseEvidenceArrayVm(evidenceRaw),
    severity: parseSeverityVm(severityRaw),
    confidence:
      typeof confidenceRaw === "number" && Number.isFinite(confidenceRaw)
        ? Math.max(0, Math.min(1, confidenceRaw))
        : undefined,
  };

  if (
    base.safe === 0 &&
    base.nudity === 0 &&
    base.sexual === 0 &&
    base.violence === 0 &&
    base.hate === 0
  ) {
    base.safe = 1;
  }

  return applyContextSignals(base, detectedLabels);
}

function parseModerationJson(raw: string): {
  scores: ParsedModeration;
  failed: boolean;
} {
  const cleaned = raw
    .replace(/^\uFEFF/, "")
    .replace(/```json\s*/gi, "")
    .replace(/```\s*/g, "")
    .trim();
  const match = cleaned.startsWith("{")
    ? cleaned
    : cleaned.match(/\{[\s\S]*\}/)?.[0];
  if (!match) return { scores: { ...SAFE_PASS_SCORES }, failed: true };
  try {
    const parsed = JSON.parse(match) as Record<string, unknown>;

    const primaryCategoryRaw =
      parsed.primary_category ?? parsed.primaryCategory;
    if (
      !primaryCategoryRaw ||
      primaryCategoryRaw === "null" ||
      primaryCategoryRaw === "none"
    ) {
      return { scores: { ...EMPTY_SCORES, safe: 1 }, failed: false };
    }

    if (!Array.isArray(parsed.evidence)) {
      return { scores: { ...EMPTY_SCORES, safe: 1 }, failed: false };
    }

    const scores = scoresFromParsedRecord(parsed);
    return { scores, failed: false };
  } catch {
    return { scores: { ...SAFE_PASS_SCORES }, failed: true };
  }
}

function extractGeminiText(data: unknown): string {
  const d = data as {
    candidates?: Array<{
      content?: { parts?: Array<{ text?: string; thought?: boolean }> };
    }>;
  };
  const parts = d?.candidates?.[0]?.content?.parts ?? [];
  return parts
    .filter((p) => p?.thought !== true)
    .map((p) => p?.text ?? "")
    .join("")
    .trim();
}

function geminiSafetyBlock(data: unknown): {
  blocked: boolean;
  reason?: string;
  category?: string;
} {
  const d = data as {
    promptFeedback?: {
      blockReason?: string;
      safetyRatings?: Array<{ category?: string; blocked?: boolean }>;
    };
    candidates?: Array<{
      finishReason?: string;
      safetyRatings?: Array<{ category?: string; blocked?: boolean }>;
    }>;
  };
  if (d?.promptFeedback?.blockReason) {
    return {
      blocked: true,
      reason: d.promptFeedback.blockReason,
      category: d.promptFeedback.safetyRatings?.find((r) => r.blocked)
        ?.category,
    };
  }
  const cand = d?.candidates?.[0];
  if (
    cand?.finishReason === "SAFETY" ||
    cand?.finishReason === "RECITATION" ||
    cand?.finishReason === "PROHIBITED_CONTENT"
  ) {
    return {
      blocked: true,
      reason: cand.finishReason,
      category: cand.safetyRatings?.find((r) => r.blocked)?.category,
    };
  }
  return { blocked: false };
}

async function callGemini(
  apiKey: string,
  parts: Array<Record<string, unknown>>,
  responseMimeType?: "application/json",
): Promise<{
  text: string;
  safetyBlocked: boolean;
  safetyBlockReason?: string;
  safetyBlockCategory?: string;
}> {
  // Guard: always include at least one text part to avoid Gemini 400 errors.
  const hasText = parts.some((p) => typeof p.text === "string" && p.text.length > 0);
  const safeParts = hasText
    ? parts
    : [
        ...parts,
        { text: "Analyze this frame from a video and describe context, setting, and activity." },
      ];
  const url = `https://generativelanguage.googleapis.com/v1/models/${GEMINI_MODEL}:generateContent?key=${apiKey}`;
  const resp = await fetch(url, {
    method: "POST",
    headers: { "Content-Type": "application/json" },
    body: JSON.stringify({
      contents: [{ parts: safeParts }],
      generationConfig: {
        temperature: 0.1,
        maxOutputTokens: responseMimeType ? 4096 : 8192,
        ...(responseMimeType ? { responseMimeType } : {}),
      },
      safetySettings: [
        { category: "HARM_CATEGORY_HARASSMENT", threshold: "BLOCK_NONE" },
        { category: "HARM_CATEGORY_HATE_SPEECH", threshold: "BLOCK_NONE" },
        {
          category: "HARM_CATEGORY_SEXUALLY_EXPLICIT",
          threshold: "BLOCK_NONE",
        },
        {
          category: "HARM_CATEGORY_DANGEROUS_CONTENT",
          threshold: "BLOCK_NONE",
        },
        { category: "HARM_CATEGORY_CIVIC_INTEGRITY", threshold: "BLOCK_NONE" },
      ],
    }),
  });
  if (!resp.ok) {
    const errText = await resp.text().catch(() => "unknown");
    throw new Error(`Gemini API ${resp.status}: ${errText}`);
  }
  const data = await resp.json();
  const safety = geminiSafetyBlock(data);
  return {
    text: extractGeminiText(data),
    safetyBlocked: safety.blocked,
    safetyBlockReason: safety.reason,
    safetyBlockCategory: safety.category,
  };
}

async function moderateImageBase64(
  apiKey: string,
  base64: string,
  mime = "image/jpeg",
): Promise<GeminiModerationResult> {
  const safeMime = normalizeImageMime(mime);
  let cleanBase64: string;
  try {
    cleanBase64 = validateAndCleanBase64(base64, "moderateImageBase64");
  } catch (err) {
    const msg = err instanceof Error ? err.message : String(err);
    console.error("[VideoModeration] base64 validation failed:", msg);
    return {
      scores: { ...SAFE_PASS_SCORES },
      classificationFailed: true,
      safetyBlocked: false,
    };
  }

  console.log("[VideoModeration] Gemini image request", {
    mimeType: safeMime,
    base64Length: cleanBase64.length,
    approxBytes: Math.round(cleanBase64.length * 0.75),
    startsWithDataPrefix: base64.startsWith("data:"),
  });

  const out = await callGemini(
    apiKey,
    [
      { text: IMAGE_MODERATION_PROMPT },
      { inlineData: { mimeType: safeMime, data: cleanBase64 } },
    ],
    "application/json",
  );
  if (out.safetyBlocked) {
    return {
      scores: { ...SAFE_PASS_SCORES },
      classificationFailed: true,
      safetyBlocked: true,
      safetyBlockReason: out.safetyBlockReason,
      safetyBlockCategory: out.safetyBlockCategory,
    };
  }
  const parsed = parseModerationJson(out.text);
  return {
    scores: parsed.scores,
    reason: parsed.scores.reason,
    detectedLabels: parsed.scores.detectedLabels,
    userSummary: parsed.scores.userSummary,
    reasonInternal: parsed.scores.reasonInternal,
    classificationFailed: parsed.failed,
    safetyBlocked: false,
  };
}

async function moderateText(
  apiKey: string,
  text: string,
): Promise<GeminiModerationResult> {
  if (!text.trim()) {
    return {
      scores: { ...EMPTY_SCORES },
      classificationFailed: false,
      safetyBlocked: false,
    };
  }
  const out = await callGemini(
    apiKey,
    [{ text: TEXT_MODERATION_PROMPT + text }],
    "application/json",
  );
  if (out.safetyBlocked) {
    return {
      scores: { ...SAFE_PASS_SCORES },
      classificationFailed: true,
      safetyBlocked: true,
      safetyBlockReason: out.safetyBlockReason,
      safetyBlockCategory: out.safetyBlockCategory,
    };
  }
  const parsed = parseModerationJson(out.text);
  return {
    scores: parsed.scores,
    reason: parsed.scores.reason,
    detectedLabels: parsed.scores.detectedLabels,
    userSummary: parsed.scores.userSummary,
    reasonInternal: parsed.scores.reasonInternal,
    classificationFailed: parsed.failed,
    safetyBlocked: false,
  };
}

function rekognitionRegion(): string {
  return (
    process.env.AWS_REKOGNITION_REGION ||
    process.env.AWS_DEFAULT_REGION ||
    "us-east-1"
  );
}

let rekognitionClient: RekognitionClient | null = null;
function getRekognitionClient(): RekognitionClient | null {
  const id = process.env.AWS_ACCESS_KEY_ID;
  const secret = process.env.AWS_SECRET_ACCESS_KEY;
  if (!id || !secret) return null;
  if (!rekognitionClient) {
    rekognitionClient = new RekognitionClient({
      region: rekognitionRegion(),
      credentials: { accessKeyId: id, secretAccessKey: secret },
    });
  }
  return rekognitionClient;
}

function mapRekognition(labels: ModerationLabel[]): ModerationScores {
  let nudity = 0;
  let sexual = 0;
  let suggestive = 0;
  let violence = 0;
  let hate = 0;
  for (const label of labels) {
    const name = (label.Name ?? "").toLowerCase();
    const parent = (label.ParentName ?? "").toLowerCase();
    const conf = (label.Confidence ?? 0) / 100;
    if (!conf) continue;
    if (
      name.includes("explicit nudity") ||
      name.includes("genitalia") ||
      name.includes("exposed breast") ||
      name.includes("exposed buttocks")
    ) {
      nudity = Math.max(nudity, conf);
      sexual = Math.max(sexual, conf * 0.9);
    }
    if (name.includes("sexual activity") || name.includes("sex toys")) {
      sexual = Math.max(sexual, conf);
    }
    if (
      parent === "suggestive" ||
      name.includes("lingerie") ||
      name.includes("implied nudity") ||
      name.includes("partial nudity")
    ) {
      suggestive = Math.max(suggestive, conf);
    }
    if (
      parent === "violence" ||
      name.includes("gore") ||
      name.includes("weapon")
    ) {
      violence = Math.max(violence, conf);
    }
    if (
      parent === "hate symbols" ||
      name.includes("nazi") ||
      name.includes("extremist")
    ) {
      hate = Math.max(hate, conf);
    }
  }
  return {
    nudity,
    sexual,
    suggestive,
    violence,
    hate,
    spam: 0,
    safe: 1 - Math.max(nudity, sexual, suggestive, violence, hate),
  };
}


function mediaKeyFromPublicUrl(url: string): string | null {
  try {
    return new URL(url).pathname
      .replace(/^\/+/, "")
      .split("/")
      .map((part) => decodeURIComponent(part))
      .join("/");
  } catch {
    return null;
  }
}

async function bodyToBuffer(body: unknown): Promise<Buffer> {
  const b = body as { transformToByteArray?: () => Promise<Uint8Array> };
  if (typeof b?.transformToByteArray === "function") {
    return Buffer.from(await b.transformToByteArray());
  }
  throw new Error("S3 object body is not readable");
}

export async function fetchVideoBuffer(
  url: string,
  storageRegion?: string,
): Promise<Buffer> {
  try {
    const resp = await fetch(url);
    if (resp.ok) {
      const buf = Buffer.from(await resp.arrayBuffer());
      if (buf.length > 0) return buf;
    }
  } catch {
    // Fall through to S3 fetch.
  }
  const key = mediaKeyFromPublicUrl(url);
  if (!key) throw new Error("Could not parse video key from URL");
  const targets = getUploadTargets();
  const target =
    (storageRegion
      ? targets.find((t) => t.region === storageRegion)
      : undefined) ?? targets[0];
  if (!target)
    throw new Error("No upload bucket target available for video moderation");
  const out = await getS3ClientForRegion(target.region).send(
    new GetObjectCommand({ Bucket: target.bucket, Key: key }),
  );
  return bodyToBuffer(out.Body);
}

// ---------------------------------------------------------------------------
// Video reasoning engine
// Runs AFTER the scoring pass. Detection (scores) stays unchanged; this layer
// only adds structured, timestamped explanations for flagged content.
// ---------------------------------------------------------------------------

/** Minimum violation score for a frame to be included in reasoning. */
const REASONING_FLAG_THRESHOLD = 0.35;

/** Maximum gap (seconds) between flagged frames to still merge into one segment. */
const SEGMENT_GAP_SEC = 3.0;

type ScoredFrame = ModerationScores & {
  frameIndex: number;
  timestampSec: number;
  provider: string;
  reason?: string;
  detectedLabels?: string[];
  userSummary?: string;
  reasonInternal?: string;
};

type FrameGroup = {
  frames: ScoredFrame[];
  category: FlaggedSegment["category"];
  peakScore: number;
  startTime: number;
  endTime: number;
};

function dominantCategory(
  s: ModerationScores,
): FlaggedSegment["category"] | null {
  const entries: Array<[FlaggedSegment["category"], number]> = [
    ["hate", s.hate],
    ["sexual", s.sexual],
    ["nudity", s.nudity],
    ["violence", s.violence],
    ["suggestive", s.suggestive],
    ["spam", s.spam],
  ];
  const [cat, score] = entries.sort((a, b) => b[1] - a[1])[0];
  return score >= REASONING_FLAG_THRESHOLD ? cat : null;
}

/**
 * Groups flagged frames into contiguous time segments.
 * Frames within SEGMENT_GAP_SEC of each other with the same dominant
 * category are merged into one segment.
 */
function groupFramesIntoSegments(frames: ScoredFrame[]): FrameGroup[] {
  const flagged = frames.filter((f) => {
    const maxViol = Math.max(
      f.nudity,
      f.sexual,
      f.suggestive,
      f.violence,
      f.hate,
      f.spam,
    );
    return maxViol >= REASONING_FLAG_THRESHOLD;
  });
  if (flagged.length === 0) return [];

  const groups: FrameGroup[] = [];
  let current: FrameGroup | null = null;

  for (const frame of [...flagged].sort(
    (a, b) => a.timestampSec - b.timestampSec,
  )) {
    const cat = dominantCategory(frame);
    if (!cat) continue;
    const score = Math.max(
      frame.nudity,
      frame.sexual,
      frame.suggestive,
      frame.violence,
      frame.hate,
      frame.spam,
    );

    if (
      current === null ||
      frame.timestampSec - current.endTime > SEGMENT_GAP_SEC ||
      cat !== current.category
    ) {
      if (current) groups.push(current);
      current = {
        frames: [frame],
        category: cat,
        peakScore: score,
        startTime: frame.timestampSec,
        endTime: frame.timestampSec,
      };
    } else {
      current.frames.push(frame);
      current.endTime = frame.timestampSec;
      if (score > current.peakScore) current.peakScore = score;
    }
  }
  if (current) groups.push(current);
  return groups;
}


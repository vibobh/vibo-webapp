export function mergeScores(scores: ModerationScores[]): ModerationScores {
  if (scores.length === 0) return { ...SAFE_PASS_SCORES };
  return scores.reduce<ModerationScores>(
    (acc, s) => ({
      nudity: Math.max(acc.nudity, s.nudity),
      sexual: Math.max(acc.sexual, s.sexual),
      suggestive: Math.max(acc.suggestive, s.suggestive),
      violence: Math.max(acc.violence, s.violence),
      hate: Math.max(acc.hate, s.hate),
      spam: Math.max(acc.spam, s.spam),
      safe: Math.min(acc.safe, s.safe),
    }),
    {
      nudity: 0,
      sexual: 0,
      suggestive: 0,
      violence: 0,
      hate: 0,
      spam: 0,
      safe: 1,
    },
  );
}

function sampleCount(
  durationSec: number | undefined,
  _trustTier: ModerationTrustTier,
): number {
  const d = durationSec && durationSec > 0 ? durationSec : 10;
  if (d > 60) return 3;
  return 4;
}

function sampleTimestamps(
  durationSec: number | undefined,
  count: number,
): number[] {
  const d = durationSec && durationSec > 0 ? durationSec : 10;
  const pcts =
    d > 60 ? [0.1, 0.3, 0.5] : [0.1, 0.3, 0.5, 0.7, 0.9];
  return pcts.slice(0, count).map((p) => Math.max(0.5, d * p));
}

function ffmpegBin(): string {
  if (!ffmpegStatic) throw new Error("ffmpeg-static did not resolve a binary");
  return ffmpegStatic;
}

async function runFfmpeg(args: string[], timeoutMs: number): Promise<void> {
  await new Promise<void>((resolve, reject) => {
    const child = spawn(ffmpegBin(), args, {
      stdio: ["ignore", "ignore", "pipe"],
    });
    let err = "";
    const timer = setTimeout(() => {
      child.kill("SIGKILL");
      reject(new Error(`ffmpeg timed out after ${timeoutMs}ms`));
    }, timeoutMs);
    child.stderr.on("data", (chunk) => {
      err += String(chunk).slice(0, 2000);
    });
    child.on("error", (e) => {
      clearTimeout(timer);
      reject(e);
    });
    child.on("close", (code) => {
      clearTimeout(timer);
      if (code === 0) resolve();
      else reject(new Error(`ffmpeg exited ${code}: ${err.slice(-1200)}`));
    });
  });
}

/** Validate that a buffer starts and ends with the JPEG magic bytes. */
function isValidJpeg(buf: Buffer): boolean {
  return (
    buf.length >= 4 &&
    buf[0] === 0xff &&
    buf[1] === 0xd8 &&
    buf[buf.length - 2] === 0xff &&
    buf[buf.length - 1] === 0xd9
  );
}

/**
 * Extract a single JPEG frame from `videoPath` at `timestampSec` into `outPath`.
 *
 * Uses `-skip_frame nokey` so FFmpeg only decodes keyframes — this prevents
 * corrupt / black / half-decoded frames that arise when a timestamp falls on a
 * B-frame or P-frame deep inside a long-GOP segment.
 *
 * Returns `true` when a valid JPEG was written, `false` otherwise.
 */
async function extractSingleFrame(
  videoPath: string,
  timestampSec: number,
  outPath: string,
): Promise<boolean> {
  try {
    await runFfmpeg(
      [
        "-y",
        "-skip_frame",
        "nokey",
        "-ss",
        timestampSec.toFixed(3),
        "-i",
        videoPath,
        "-frames:v",
        "1",
        "-vf",
        "scale=512:-1",
        "-q:v",
        "5",
        "-f",
        "image2",
        outPath,
      ],
      20_000,
    );
  } catch (err) {
    console.warn("[VideoModeration] FFmpeg frame extraction error", {
      timestamp: timestampSec,
      error: err instanceof Error ? err.message : String(err),
    });
    return false;
  }

  // Confirm the file was written and contains a valid JPEG.
  try {
    const buf = await readFile(outPath);
    if (buf.length < 10_000 || !isValidJpeg(buf)) {
      console.warn(
        "[VideoModeration] Frame invalid or empty after extraction",
        {
          timestamp: timestampSec,
          byteLength: buf.length,
          headerOk: buf.length >= 2 && buf[0] === 0xff && buf[1] === 0xd8,
        },
      );
      return false;
    }
    return true;
  } catch {
    return false;
  }
}

export async function extractFramesFromVideo(args: {
  videoPath: string;
  workDir: string;
  durationSec?: number;
  trustTier: ModerationTrustTier;
}): Promise<ExtractedFrame[]> {
  const timestamps = sampleTimestamps(
    args.durationSec,
    sampleCount(args.durationSec, args.trustTier),
  );
  const frames: ExtractedFrame[] = [];

  for (let i = 0; i < timestamps.length; i++) {
    const base = join(args.workDir, `frame-${String(i).padStart(2, "0")}.jpg`);

    // Try up to 3 times with a 0.5 s forward offset on each retry.  This
    // recovers from timestamps that land between keyframes on long-GOP video.
    let succeeded = false;
    for (let attempt = 0; attempt < 3; attempt++) {
      const ts = timestamps[i] + attempt * 0.5;
      const ok = await extractSingleFrame(args.videoPath, ts, base);
      if (ok) {
        frames.push({ path: base, timestampSec: ts, index: i });
        succeeded = true;
        break;
      }
      console.warn("[VideoModeration] Frame attempt failed, retrying", {
        frameIndex: i,
        attempt,
        timestamp: ts,
      });
    }

    if (!succeeded) {
      console.warn(
        "[VideoModeration] All attempts failed for frame, skipping",
        { frameIndex: i, baseTimestamp: timestamps[i] },
      );
    }
  }

  return frames;
}

export async function moderateFrame(args: {
  apiKey: string;
  framePath: string;
  frameIndex: number;
  timestampSec: number;
}): Promise<
  ModerationScores & {
    frameIndex: number;
    timestampSec: number;
    provider: string;
    reason?: string;
    detectedLabels?: string[];
    userSummary?: string;
    reasonInternal?: string;
    classificationFailed?: boolean;
    safetyBlocked?: boolean;
    safetyBlockReason?: string;
    safetyBlockCategory?: string;
  }
> {
  // Guard 1: file must exist and be large enough to be a real JPEG.
  let fileSize = 0;
  try {
    const s = await stat(args.framePath);
    fileSize = s.size;
  } catch {
    console.warn("[VideoModeration] Frame file missing, skipping", {
      framePath: args.framePath,
      frameIndex: args.frameIndex,
    });
    return {
      ...SAFE_PASS_SCORES,
      frameIndex: args.frameIndex,
      timestampSec: args.timestampSec,
      provider: "fail_safe",
      classificationFailed: true,
      reason: "analysis_failed_safe_fallback",
    };
  }

  if (fileSize < 10_000) {
    console.warn("[VideoModeration] Frame too small, skipping Gemini", {
      framePath: args.framePath,
      frameIndex: args.frameIndex,
      byteLength: fileSize,
    });
    return {
      ...SAFE_PASS_SCORES,
      frameIndex: args.frameIndex,
      timestampSec: args.timestampSec,
      provider: "fail_safe",
      classificationFailed: true,
      reason: "analysis_failed_safe_fallback",
    };
  }

  const bytes = await readFile(args.framePath);

  // Guard 2: double-check buffer length and JPEG integrity after read.
  if (bytes.length < 10_000 || !isValidJpeg(bytes)) {
    console.warn(
      "[VideoModeration] Frame buffer invalid after read, skipping",
      {
        framePath: args.framePath,
        frameIndex: args.frameIndex,
        byteLength: bytes.length,
        headerOk: bytes.length >= 2 && bytes[0] === 0xff && bytes[1] === 0xd8,
        footerOk:
          bytes.length >= 2 &&
          bytes[bytes.length - 2] === 0xff &&
          bytes[bytes.length - 1] === 0xd9,
      },
    );
    return {
      ...SAFE_PASS_SCORES,
      frameIndex: args.frameIndex,
      timestampSec: args.timestampSec,
      provider: "fail_safe",
      classificationFailed: true,
      reason: "analysis_failed_safe_fallback",
    };
  }

  // Guard 2b: reject frames that are unreasonably large for Gemini inline data.
  if (bytes.length > 1_000_000) {
    console.warn("[VideoModeration] Frame buffer too large, skipping Gemini", {
      framePath: args.framePath,
      frameIndex: args.frameIndex,
      byteLength: bytes.length,
    });
    return {
      ...SAFE_PASS_SCORES,
      frameIndex: args.frameIndex,
      timestampSec: args.timestampSec,
      provider: "fail_safe",
      classificationFailed: true,
      reason: "analysis_failed_safe_fallback",
    };
  }

  const base64 = bytes.toString("base64");
  const cleanBase64 = base64.replace(/^data:image\/\w+;base64,/, "");

  // Guard 3: base64 string must be long enough to represent real image data.
  if (!cleanBase64 || cleanBase64.length < 1_000) {
    console.warn("[VideoModeration] Frame base64 too short, skipping Gemini", {
      framePath: args.framePath,
      frameIndex: args.frameIndex,
      base64Length: cleanBase64?.length ?? 0,
    });
    return {
      ...SAFE_PASS_SCORES,
      frameIndex: args.frameIndex,
      timestampSec: args.timestampSec,
      provider: "fail_safe",
      classificationFailed: true,
      reason: "analysis_failed_safe_fallback",
    };
  }

  console.log("FRAME DEBUG", {
    frameIndex: args.frameIndex,
    timestampSec: args.timestampSec,
    path: args.framePath,
    size: bytes.length,
    base64Length: cleanBase64.length,
    preview: cleanBase64.slice(0, 40),
  });

  try {
    const gemini = await moderateImageBase64(
      args.apiKey,
      cleanBase64,
      "image/jpeg",
    );
    if (gemini.safetyBlocked) {
      console.warn("[VideoModeration] Frame safety-blocked by Gemini", {
        frameIndex: args.frameIndex,
        safetyBlockReason: gemini.safetyBlockReason,
      });
      return {
        ...SAFE_PASS_SCORES,
        frameIndex: args.frameIndex,
        timestampSec: args.timestampSec,
        provider: "gemini_safety_blocked",
        safetyBlocked: true,
        safetyBlockReason: gemini.safetyBlockReason,
        safetyBlockCategory: gemini.safetyBlockCategory,
        classificationFailed: true,
      };
    }
    if (gemini.classificationFailed) {
      console.warn("[VideoModeration] Frame classification failed", {
        frameIndex: args.frameIndex,
      });
      return {
        ...SAFE_PASS_SCORES,
        frameIndex: args.frameIndex,
        timestampSec: args.timestampSec,
        provider: "fail_safe",
        reason: "analysis_failed_safe_fallback",
        classificationFailed: true,
      };
    }
    return {
      ...gemini.scores,
      frameIndex: args.frameIndex,
      timestampSec: args.timestampSec,
      provider: "gemini",
      reason: gemini.reason,
      detectedLabels: gemini.detectedLabels,
      userSummary: gemini.scores.userSummary,
      reasonInternal: gemini.scores.reasonInternal,
    };
  } catch (err: any) {
    const msg = err instanceof Error ? err.message : String(err);
    console.error("GEMINI FRAME ERROR", {
      path: args.framePath,
      frameIndex: args.frameIndex,
      size: bytes?.length,
      base64Length: cleanBase64?.length,
      error: err?.response?.data || msg,
    });
    return {
      ...SAFE_PASS_SCORES,
      frameIndex: args.frameIndex,
      timestampSec: args.timestampSec,
      provider: "fail_safe",
      reason: "analysis_failed_safe_fallback",
      classificationFailed: true,
    };
  }
}

